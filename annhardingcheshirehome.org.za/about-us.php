           ABOUT US
           <h4>Vision, Mission and Values</h4>
           <p>Ann Harding Cheshire Home is a registered Non-Profit Organisation for the care, education, training and development of persons with physical disabilities of all races and religions.
            Our inspiration is our 40 Residents.  The people who, despite their physical disabilites maintain a sense of humour.  Those Residents who, despite their physical limitations complete 5km in the 702 Walk the Talk every  year.  The Residents whose lives were changed in an instant, victims of crime, or motor vehicle accidents.  The small percentage who have genetic illnesses, or those who were struck down in their prime with a debilitating illness.  It could be any one of us.  The majority of our Residents never thought they would find themselves living in a Home for the Physically Disabled.
            </p>
            <h4>History of the Ann Harding Cheshire Home</h4>
            <p>Ann Harding Cheshire Homes was opened in 1976 in a house and land donated by the Council.  The house, which is now the Charity Shop, was only large enough to house eight Residents, and even that was tight.</p>
            <p>Development of the surrounding land only took place much later, and in 1988 a building to house 40 Residents was opened.</p>
            <p>The Home is named after the physically disabled mother of one Benjamin Charles Harding who originally donated property for the Home which proved unsuitable for development.  This property was sold, and the money used to develop the current land.</p>
            <h4>Primary Services</h4>
            <p>We provide our Residents, all of whom live here permanently, with comfortable accommodation, 24 hour care, nutritious meals, laundry, access to therapists and social worker as well as physiotherapists, state clinics and hospitals and private practitioners using our own specially adapted vehicles.</p>
            <p>The majority of our 40 residents have taken up permanent residence having suffered from, and becoming permanently disabled as a result of:</p>
            <ul>
                <li>Motor vehicle accidents</li>
                <li>Violence and assault</li>
                <li>Strokes</li>
                <li>Degenerative diseases</li>
                <li>Congenital disabilities</li>
            </ul>
            <p>A wide variety of daily activities keeps our Residents motivated and educated.  The more popular ones are arts and crafts, painting, bingo, chess and card games.  More recently (2014), we have upgraded our swimming pool, which is now a heated therapy pool.  Residents are enjoying spending time in the pool with our full-time Occupational Therapist, as well a private Physiotherapists.</p>
            <h4>FUTURE PLANS</h4>
