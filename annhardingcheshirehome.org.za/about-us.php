            <h4>WHAT WE CAN OFFER</h4>
            <p>Ann Harding Cheshire Home has a committee of dedicated members who ensure exemplary ethics and code of conduct, and that any funds received are used for the intended purpose.  Monthly reports detail the progress made in the utilisation of all donations, sponsorships and bequests made.
